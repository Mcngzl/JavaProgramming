package day03_EscapeSequences;
/*
Escape sequences:MUST
\n :starts a new line
\t :tab
\\: single backslash be printed
\" : double quotes
 */

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nphyton \n C#");
        System.out.println("--------------------------");
        System.out.println("Hello cydeo \nhow are you today? \nIts  nice to see you all!");
        System.out.println("------------------------------------------");
        System.out.println("\tJava is cool Programming Language");
        System.out.println("--------------------------------------------");
        System.out.println("/ \\");// in order to print one backward slash we need to give two backward slash
        System.out.println("---------------------------------------------");
        System.out.println("My favorite TV show is \"Game Of Thrones\"");//n orderto print double quotes \"
    }



}
